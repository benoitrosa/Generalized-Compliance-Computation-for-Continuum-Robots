function deltaij = delta_ij(i,j)
    
% ======================================================================= %
% ======================================================================= %
%
% This function is the Kronecker delta
%
% ====================
% ====== INPUTS ====== 
%
% i         : (int ∊ [1 , nbT])
% j         : (int ∊ [1 , nbT])
%
% ====================
% ===== OUTPUTS ====== 
%
% deltaij   : (int) 0 or 1 only
%
% ======================================================================= %
% ======================================================================= %

    deltaij = (i == j) ;
    
end

