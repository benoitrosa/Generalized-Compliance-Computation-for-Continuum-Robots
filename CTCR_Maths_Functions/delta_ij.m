function deltaij = delta_ij(i,j)
    
% ======================================================================= %
% ======================================================================= %

% This function is the Kronecker delta

% ====================
% ====== INPUTS ====== 

% i         : [index]
% j         : [index]

% ====================
% ===== OUTPUTS ====== 

% deltaij   : [int] which can take the value 0 or 1

% ======================================================================= %
% ======================================================================= %

    deltaij = (i == j) ;
    
end

